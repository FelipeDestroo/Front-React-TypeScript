// 1 - Number
console.log(typeof 4)
console.log(typeof 2.87)
console.log(typeof -55)

// 2 - Operações Aritméticas
console.log(2 + 9)
console.log(2 - 9)
console.log(2 * (8 + 9))
console.log(10 / 2 + (2 + 3))

// 3 - Special Numbers
console.log(typeof Infinity)
console.log(typeof -Infinity)
// console.log(4 * hello)
console.log(typeof NaN)

// 4 - String
console.log("Hello World!");
console.log(`Mais um texto`);
console.log(typeof `144`);

// 5 - Símbolos especiais em string
console.log("Testando a \nquebra de linha");
console.log("Espaçamento de \t tab");

// 6 - Concatenação
console.log("Olá," + " mundo" + "!");
console.log(`Testando` + ` com` + ` crase`);

// 7 - Interpolação
console.log(`A soma de 5 * 2 é: ${5 * 2}`);

// 8 - Boolean
console.log(true);
console.log(false);
console.log(30 > 2);
console.log(90<100);
console.log(typeof true);

// 9 - Comparações
console.log(5 <= 5);
console.log(5 < 5);
console.log(10 == 10);
console.log(10 == 9);
console.log(20 != 20);

//  10 - Idêntico
console.log(9 == "9") // !
console.log(9 === "9")
console.log(9 != "9")
console.log(9 !== "9")

// 11 - Operadores lógicos
console.log(true && true);
console.log(10 < 20 && 20 > 100)
console.log(20 > 2 && "Felipe" === Number)
console.log(2 + 8 == 10 || 10 === 10)
console.log(!2 < 10)
console.log(!10 > 11)

// 12 - Empty value
console.log(typeof null, typeof undefined);
console.log(null === undefined);
console.log(null == undefined);
console.log(null == false);
console.log(undefined == false);

// 13 - Conv. Automática
console.log(5 * null)
